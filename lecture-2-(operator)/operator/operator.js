// assignment operator
// (=, +=, -=, /=, *=)

let a = 10;
let b = a;
b += 1;
 console.log(b);
// arithmetic operator
// (+, -, /, *, %)

let num1 = 10; 
let num2 = 20;

console.log(num1 + num2);
console.log(num2 - num1);
console.log(num1 * num2);
console.log(num2 / num1);
console.log(num2 % num1);

// logical operator

//(&& ||)

let flag1 = true; 
let flag2 = false;

console.log(flag1 && flag2)
console.log(flag1 || flag2)

//comparison operator (<, >, <=, >=, ==, ===) triple equals to is used for strict checking between two variables 

let g = a == b;
let h = a < b;
let i = a > b;
let j = a === b;

console.log(g)
console.log(h)
console.log(i)
console.log(j)
